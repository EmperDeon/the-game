 public class ItemsTable implements TableModel {

  private final Set<TableModelListener> listeners = new HashSet<>();

  private final ArrayList<IItem> items = new ArrayList<>();

  @Override
  public void addTableModelListener ( TableModelListener listener ) {
   listeners.add(listener);
  }

  @Override
  public Class<?> getColumnClass ( int columnIndex ) {
   return String.class;
  }

  @Override
  public int getColumnCount () {
   return 6;
  }

  @Override
  public String getColumnName ( int columnIndex ) {
   switch ( columnIndex ) {
    case 0:
     return "I name";
    case 1:
     return "S name";
    case 2:
     return "Durability";
    case 3:
     return "Model";
    case 4:
     return "Type";
    case 5:
     return "Speeds";
   }
   return "";
  }

  @Override
  public int getRowCount () {
   return items.size();
  }

  @Override
  public Object getValueAt ( int rowIndex , int columnIndex ) {
   switch ( columnIndex ) {
    case 0:
     return items.get(rowIndex).getId().getIid();
    case 1:
     return items.get(rowIndex).getId().getSid();
    case 2:
     return items.get(rowIndex).getDurab().toString();
    case 3:
     return items.get(rowIndex).getModel().toString();
    case 4:
     return items.get(rowIndex).getType().toString();
    case 5:
     return items.get(rowIndex).getSpeed().toString();

   }
   return "";
  }

  @Override
  public boolean isCellEditable ( int rowIndex , int columnIndex ) {
   return false;
  }

  @Override
  public void removeTableModelListener ( TableModelListener listener ) {
   listeners.remove(listener);
  }

  @Override
  public void setValueAt ( Object value , int rowIndex , int columnIndex ) {

  }

  public void add () {
   IItem t = new IItem(new Mid(modname.getText() , iiname.getText(), isname.getText()),Integer.parseInt(idurab.getText()), new Model(imodel.getText()), Integer.parseInt(itype.getText()), new Speeds(ispeed.getText()) );
   this.items.add(t);
  }

 }

